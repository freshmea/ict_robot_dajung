# description: 연산자 출력하기
def main():
    print(123)
    print(123.456)
    print(type(123))
    print(type(123.456))
    print(2**2)
    print(2**64)

    # 나누기 연산
    print(18 / 3)
    print(type(18 / 3))

    print(18 // 3)
    print(type(18 // 3))

    print(14 / 3)
    print(14 // 3)
    print(14 % 3)


if __name__ == "__main__":
    main()
